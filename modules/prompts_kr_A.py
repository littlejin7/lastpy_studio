STYLE_EXAMPLES = {
    "1020 (귀염/긍정 텐션)": "말 끝에 '~해떠', '~라구'를 쓰고, 감탄사를 '우와아'처럼 귀엽게 표현하세요. 욕설은 절대 금지입니다.",
    "3040 (핵심요약/효율)": "논리적이고 깔끔한 문어체를 사용하세요. 사회적 유행의 원인을 분석하는 관점을 유지하세요.",
    "5060 (긍정/따뜻한 연륜)": "옆집 이모처럼 구수하고 따뜻한 말투를 사용하세요. 동물을 사랑하고 아끼는 마음을 담으세요."
}

def get_translation_prompt_A(persona_key, context_data):
    style_guide = STYLE_EXAMPLES.get(persona_key, "")
    
    return f"""
    당신은 유튜브 쇼츠 콘텐츠 크리에이터입니다.
    제공된 최신 데이터를 바탕으로 페르소나에 맞는 [60초 한글 스크립트]를 재창작하세요.

    [페르소나 가이드]: {style_guide}
    [선택된 제목]: {context_data['titles']}
    [최신 팩트 데이터]: {context_data['trends']}

    [🚫 절대 금지 및 강제 규칙]
    1. **영어 사용 0%**: 'Visual:', 'Action:', 'Scene:' 등 모든 영어 헤더를 삭제하고 '(화면:)', '(상황:)'으로만 표기하세요.
    2. **용어 강제 치환**: 
       - 'Try' -> '만나보다' / 'Taste' -> '매력' (동물 주제일 때 절대 '먹다' 금지)
       - 모든 영어 단어는 적절한 한국어 구어체로 의역하세요.
    3. **해시태그**: 영어 해시태그는 모두 삭제하고, 주제와 한국 정서에 맞는 **순수 한글 해시태그 10개**를 새로 만드세요.
    4. **루프 설계**: 마지막 문장은 첫 문장과 자연스럽게 이어지도록 마침표 없이 끝내세요.

    [출력 양식]
    제목: 
    [00:00] (화면 설명) 대사
    ...
    해시태그: 
    """